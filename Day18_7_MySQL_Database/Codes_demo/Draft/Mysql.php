1 - Tạo cơ sở dữ liệu
- Nhập tên CSDL và chọn chế độ utf8_unicode_ci

2 - Tạo bảng
- Thường tên bảng sẽ ở dạng số nhiều, Vd: users, questions
Tips: có thể dựa vào giao diện để phân tích xem 1 đối tượng
(sản phẩm, tin tức) có các cột (field/column) nào

3- Bốn thao tác căn bản với cơ sở dữ liệu nói chung
- Lấy dữ liệu - Insert
- Cập nhật dữ liệu - Update
- Xóa dữ liêu - Delete
- Xem dữ liệu - View/Detail

4 - Thường khi tạo cơ sở dữ liệu và
cấu trúc bảng (tên cột, kiểu dữ liệu, null hay ko,
...) sẽ dùng giao diện đồ họa
(như Phpmyadmin, Mysql Workbench, Navicat) để tạo

